# você pode ESCAPAR aspas duplas ultilizando aspas simples
# vale refoçar que isso so vale para aspas duplas, outros caracteres não. 
# texto1 recebe o valor "sou do"
texto1 = '"sou do"'  

# texto2 recebe o valor "back-end"
# caracter de SCAPE é o /
texto2 = "\"back-end\""

# a função print, concatena e imprime as duas variaveis na tela 
print(texto1, texto2)